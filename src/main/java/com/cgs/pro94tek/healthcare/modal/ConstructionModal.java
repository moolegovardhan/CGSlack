package com.cgs.pro94tek.healthcare.modal;

public class ConstructionModal  {
	

	private Long id;
	private String ownerName;
	private String userid;
	private String modalhouse;
	private String bedroom;
	private String elevent;
	private String apartmentFlats;
	private String layouts;
	
	public String getApartmentFlats() {
		return apartmentFlats;
	}
	public void setApartmentFlats(String apartmentFlats) {
		this.apartmentFlats = apartmentFlats;
	}
	public String getLayouts() {
		return layouts;
	}
	public void setLayouts(String layouts) {
		this.layouts = layouts;
	}
	
	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getModalhouse() {
		return modalhouse;
	}
	public void setModalhouse(String modalhouse) {
		this.modalhouse = modalhouse;
	}
	public String getBedroom() {
		return bedroom;
	}
	public void setBedroom(String bedroom) {
		this.bedroom = bedroom;
	}
	public String getElevent() {
		return elevent;
	}
	public void setElevent(String elevent) {
		this.elevent = elevent;
	}
	


}
